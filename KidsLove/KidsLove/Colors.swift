import Foundation
import UIKit

extension UIColor {
    
    static func niceRed() -> UIColor {
        return UIColor(red: 1.0, green: 0.25, blue: 0.0, alpha: 1.0)
    }
    
    static func springBlue() -> UIColor {
        return UIColor(red: 0.7, green: 0.8, blue: 1.0, alpha: 1.0)
    }
    
    static func writeGreen() -> UIColor {
        return UIColor(red: 0.7, green: 1.0, blue: 0.6, alpha: 1.0)
    }
    
    static func heavyGreen() -> UIColor {
        //return UIColor(red: 0, green: 1.0, blue: 0.0, alpha:1.0)
        return .systemGreen
    }
}
